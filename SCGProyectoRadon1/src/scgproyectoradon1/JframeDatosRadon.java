    protected void graficar(){
        
       
        
        JFreeChart grafica_dispersion = ChartFactory.createScatterPlot(
                "grafico dispersion",
                "tiempo",
                "concentracion",
                dataSet(),
                PlotOrientation.VERTICAL,
                true,
                true,
                false);
        
        ChartPanel panelG = new ChartPanel(grafica_dispersion);
        panelG.setDomainZoomable(true);
        panelG.setVisible(true);
        
        jpanel_Grafica.setLayout(new BorderLayout());
        jpanel_Grafica.add(panelG,BorderLayout.CENTER);
        jpanel_Grafica.updateUI();
        
        
    }
    private XYDataset dataSet() {
        
        final XYSeries XYConcentracion = new XYSeries("Concentración");

        int i=0;
        
        
        for (Datos datos : datos.getDatosRadon()) {
            XYConcentracion.add(i++, datos.getConcentracion());
            
            
        }
          
        final XYSeriesCollection dataSet = new XYSeriesCollection();
           
         dataSet.addSeries(XYConcentracion);
     
        
       
        
        return dataSet;
    }

    
    
    

