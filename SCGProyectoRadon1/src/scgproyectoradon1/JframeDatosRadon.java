    /**
     * Invoca a la clase Conexion para realizar la busqueda de datos 
     * y los almacena en la clase Datos
     **/
    public void buscarDatos(){
        
    // obtener los datos deacuerdo a los filtros dados

        datos.setDatosRadon(conexion.getDatos(datos.getEstacion(), datos.getFechaDesde(), datos.getFechaHasta()))  ;
        if(!datos.getDatosRadon().isEmpty()){
            //envio los datos capturados a verificar si existen datos atipicos
            ArrayList<Double> datosAtipicos = new ArrayList<>();


            datosAtipicos = calcularDatosAtipicos.Calcular_Datos_Atipicos(datos.getDatosRadon());

            // buscamos datos atipicos si existen

            if(!datosAtipicos.isEmpty()){
                JOptionPane.showMessageDialog(null, "Se encontraron algunos posibles Datos Atipicos");
                
                cargarDatos(conexion.getDatosAtipicos(datosAtipicos,datos.getEstacion(), datos.getFechaDesde(), datos.getFechaHasta()),true,false);

            }else{
                JOptionPane.showMessageDialog(null, "No se encontraron datos atipicos en\n\n"
                                            + "Estación: " +datos.getEstacion() +"\n"
                                            + "Fecha inicio: "+datos.getFechaDesde()+"\n"
                                            + "Fecha Fin: "+datos.getFechaHasta());

                cargarDatos(datos.getDatosRadon(),false,false);

            }
        
         graficar();
        
        }else{
            JOptionPane.showMessageDialog(null, "No se encontraron Datos \n"
                                                + "En el rango de fechas ingresadas");
            activarBotones(false);
            
        }  
    }

    
    /**

     * Carga la infomación en la tabla principal recibiendo los siguientes parametros:
     * 
     * @param datos : ArrayList de tipo Datos contiene los datos obtenidos de las consultas a la base de datos
     * @param existeAtipico : Variable de tipo Boolean que indica si los datos que ingresan son datos atipicos
     * @param mediaMovil : Variable de tipo Boolean que indica si los datos que ingresan contiene media movil
     * 
     **/
    protected void cargarDatos(ArrayList<Datos> datos,boolean existeAtipico,boolean mediaMovil) {
        if(mediaMovil)model = new DefaultTableModel(new Object[]{"#","Estación","Fecha","Hora","Concentración","Media Movil"},0);
        else model = new DefaultTableModel(new Object[]{"#","Estación","Fecha","Hora","Concentración"},0);
        
        
        if (existeAtipico){
            lbl_titulo.setText("Tabla datos Atipicos :   "+ this.datos.getEstacion()+"   "+this.datos.getFechaDesde()+"  --  "+this.datos.getFechaHasta());
            
            btn_mediaMovil.setEnabled(false);
            btn_eliminar.setEnabled(true);
            btn_descartar.setEnabled(true);
            btn_exportar.setEnabled(true);
            btn_estadisticas.setEnabled(true);
            
            jtabla_datos.removeAll();
            jtabla_datos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            jtabla_datos.setSelectionForeground(Color.RED);
            jtabla_datos.setSelectionBackground(Color.BLACK);
        }
        else {
            lbl_titulo.setText("Tabla datos :   "+ this.datos.getEstacion()+"   "+this.datos.getFechaDesde()+"  --  "+this.datos.getFechaHasta()); 
            
            btn_mediaMovil.setEnabled(true);
            btn_eliminar.setEnabled(false);
            btn_descartar.setEnabled(false);
            btn_exportar.setEnabled(true);
            btn_estadisticas.setEnabled(true);
            
            jtabla_datos.removeAll();
            jtabla_datos.setSelectionMode(ListSelectionModel.SINGLE_INTERVAL_SELECTION);
            jtabla_datos.setSelectionForeground(Color.blue);
            jtabla_datos.setSelectionBackground(Color.GREEN);
            
            
        }
        
        jtabla_datos.setModel(model);
    
       if(mediaMovil){
            for (Datos dato : datos) {
                model.addRow(new Object[]{jtabla_datos.getRowCount()+1,dato.getCodestacion(),dato.getFecha(),dato.getHora(),dato.getConcentracion(),dato.getPromediomovil()});
            }
       }else{
            for (Datos dato : datos) {
            model.addRow(new Object[]{jtabla_datos.getRowCount()+1,dato.getCodestacion(),dato.getFecha(),dato.getHora(),dato.getConcentracion()});
            }
       }
    }
    

    
