     /**
     * Abre una ventana de confirmación de eliminación de un elemento
     * seleccionado en la tabla, de acuerdo al resultado de la confirmación de eliminación
     * se elimina o se conserva el registro seleccionado
     **/
    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        
   
        double datoAtipico = Double.parseDouble(jtabla_datos.getValueAt(jtabla_datos.getSelectedRow(), 4).toString());
        int opcionEliminar = JOptionPane.showConfirmDialog(null, "Desea Eliminar el dato seleccionado?\n\n Dato: "+datoAtipico ,"Eliminar Dato Atipico",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);

        if(opcionEliminar == 0){
            conexion.eliminarDatosAtipicos(datos.getCodestacion(), datos.getFechaDesde(), datos.getFechaHasta()
                    , datoAtipico);
            model.removeRow(jtabla_datos.getSelectedRow());

            jtabla_datos.updateUI();

        }       
        
    }//GEN-LAST:event_btn_eliminarActionPerformed

